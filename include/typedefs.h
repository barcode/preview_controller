#pragma once
#include <Eigen/Core>

namespace Eigen {

#ifdef EIGEN_3_2_FOUND
using Index = Eigen::Matrix<int, 1, 1>::Index;
#endif
} // namespace Eigen

namespace mpc {

template <typename T1, typename T2>
struct IsNotIntegral {
    static const bool value = !(std::is_integral<typename std::decay<T1>::type >::value || std::is_integral<typename std::decay<T2>::type >::value);
};

} // namespace mpc